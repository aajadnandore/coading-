#include<bits/stdc++.h>
using namespace std;
int main(){
    
        int i = 65;          // Start from ASCII 65 (A)
    
        while (i <= 90) {       // Loop until ASCII 90 (Z)

            cout << "ASCII " << i << " = " << char(i) << endl;

            i++;               // Increment i
        }
    
return 0;
}